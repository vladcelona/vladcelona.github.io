/**
 * Function that formats number (price) to format 123 456.78
 * @param {number} price 
 * @returns A formatted string number
 */
function formatPrice(price) {
  return `${(price).toString().replace(/,/g, "").replace(/\B(?=(\d{3})+(?!\d))/g, " ")}`;
}

